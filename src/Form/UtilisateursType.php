                'second_options' => array('label' => 'RÃ©pÃ©ter mot de passe'),
                

            ))
            ->add('role', ChoiceType::class, array('mapped' => false, 'label' => 'Role :', 'choices' => array
            (
                "Utilisateur" => false,
                "Administrateur" => true
            )));

        ;
    }

    public function configureOptions(OptionsResolver $resolver)
    {
        $resolver->setDefaults([
            'data_class' => Utilisateurs::class,
        ]);
    }
}
